# Tipe data dasar dalam Python
# Oleh: Yunita (contoh)

# 1. Integer (bilangan bulat)
angka = 10
print("Integer:", angka, type(angka))

# 2. Float (bilangan desimal)
nilai_float = 3.14
print("Float:", nilai_float, type(nilai_float))

# 3. String (teks)
nama = "Yunita"
print("String:", nama, type(nama))

# 4. Boolean (True / False)
is_siswa = True
print("Boolean:", is_siswa, type(is_siswa))

# 5. List (daftar)
daftar_nama = ["Yunita", "Budi", "Sari"]
print("List:", daftar_nama, type(daftar_nama))

# 6. Tuple (daftar tetap)
koordinat = (10, 20)
print("Tuple:", koordinat, type(koordinat))

# 7. Dictionary (kamus / key-value)
data_siswa = {
    "nama": "Yunita",
    "umur": 17,
    "kelas": "12 IPA"
}
print("Dictionary:", data_siswa, type(data_siswa))

# 8. Set (himpunan, elemen unik)
angka_unik = {1, 2, 3, 1, 2}
print("Set:", angka_unik, type(angka_unik))  # Outputnya: {1, 2, 3}
