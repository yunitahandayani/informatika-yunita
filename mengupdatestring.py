# Contoh sederhana mengubah string
# Oleh: Yunita

teks = "Halo, saya Yunita"
print("Teks asli:", teks)

# Ganti nama
teks = teks.replace("Yunita", "Ayu")
print("Teks setelah diganti:", teks)

# Tambah kata di akhir
teks = teks + " belajar Python"
print("Teks setelah ditambah:", teks)

# Ubah jadi huruf besar
print("Huruf besar semua:", teks.upper())
